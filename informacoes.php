			<h1 align="center" style="font-family:OCR A Std, monospace; font-size:25px; ">Motus - Movimento Literário Digital<br>
Edital nº 01/2017<br><br>

		<h1 align="justify" style="font-family:OCR A Std, monospace; font-size:25px; ">

O programa de extensão Programa C - Comunidade, Computação, Cultura, Comunicação, Ciência, Cidadania, Criatividade, Colaboração - vinculados aos cursos Ciência da Computação e Engenharia de Software da Universidade Federal do Pampa (UNIPAMPA) torna público o regulamento da Motus - Movimento Literário Digital - com objetivo de incentivar e divulgar a produção de obras literárias. <br>
<br>1. DOS OBJETIVOS E DISPOSIÇÕES PRELIMINARES<br><br>
1.1 O Edital tem como objetivos:<br>
I. Incentivar a produção de obras literárias junto à comunidade acadêmica da UNIPAMPA e de indivíduos de qualquer estado, residentes no Brasil.<br>
	II. Despertar o interesse pela literatura nos estudantes e cidadãos.<br>
III. Selecionar e publicar obras literárias inéditas, em língua portuguesa.<br>
1.2 Pelo ato da inscrição, os participantes permitem que a Motus utilize as obras literárias inscritas em, por exemplo, cartazes, banners e/ou vídeos.<br>   
1.3 É de responsabilidade única, exclusiva e irrestrita do(a) participante, a veracidade das informações por ele(a) prestadas no ato da inscrição e a regularização de toda e qualquer questão relativa à Lei de Direitos Autorais (Lei Federal nº 9.610, de 19 de fevereiro de 1998) no que se refere à documentação encaminhada.<br>
1.4 O ato de inscrição é voluntário e implica na aceitação das normas e condições estabelecidas no presente Edital.<br><br>
2. DA MODALIDADE E TEMA<br><br>
2.1 Para a inscrição de obras literárias, o presente Edital contempla os gêneros literários Poema e Conto.<br>
2.2 O tema a ser abordado nas obras inscritas na primeira edição da Motus: Liberdade de Expressão.<br><br>
3. DOS REQUISITOS DE PARTICIPAÇÃO<br><br>
3.1 Os(As) participantes menores de 18 (dezoito) anos só poderão realizar a inscrição se apresentarem autorização dos pais, responsáveis ou professor(a) responsável no formato que se encontra no anexo 1.0 do edital.<br>
3.2 A inscrição em mais de uma categoria implicará em eliminação da participação em todas as categorias.<br>
3.3 Está vedada a inscrição de textos que não sejam de autoria própria.<br><br>
4. DO REGULAMENTO<br><br>
4.1 A participação neste Concurso pode ser feita individualmente ou ser realizada com coautoria de, por exemplo, algum(a) professor(a) responsável do(a) aluno(a), isto é, qualquer professor(a) que tenha ajudado o(a) aluno(a) a entender o tema ou a revisar a obra literária.<br>
4.2 Sempre que houver inadequação entre o texto e os gêneros literários presentes no Item 2.1 deste Edital, a participação será cancelada.<br>
4.3 O limite máximo de inscrições por autor(a) é de 01 (um) texto. <br>
4.4 Caso haja algum(a) coautor(a), a sua participação pode estar presente em várias obras. Por exemplo, um(a) professor(a) pode ser coautor(a) de inúmeros textos.<br>
4.5 Caso o(a) participante inscreva uma quantidade de textos superior à permitida, somente será considerado válido para efeitos de premiação o último dentre os inscritos.<br>
4.6 As obras submetidas ao Concurso devem ser inéditas, não tendo sido publicadas em nenhum tipo de mídia, seja impressa ou digital.<br>
4.7 Os textos devem ser enviados em documento word (.word ou .docx) com fonte Times New Roman, tamanho 12. Espaçamento entre linhas 1,5 e margens Normal.<br>
4.8 O texto do gênero literário poema pode ter tamanho de até duas (2) laudas.<br>
4.9 O texto do gênero literário conto pode ter até 5.000 (5 mil) caracteres, contando espaços.<br><br>
5. DAS INSCRIÇÕES<br><br>
5.1 As inscrições para o Concurso são gratuitas e serão realizadas exclusivamente pela internet, através do e-mail da Motus.<br>
Os interessados devem enviar um e-mail para motus.unipampa@gmail.com seguindo as diretrizes abaixo:<br>
O Assunto do e-mail deve ser: Inscrição - Concurso Literário <br>
O corpo do e-mail deve conter:<br>
a)	O nome do(a) participante;<br>
b)	A cidade e o Estado do(a) participante;<br>
c)	Caso necessário, fornecer o nome do(a) coautor(a) responsável pelo auxílio no texto;<br>
d)	Caso o participante seja estudante de alguma Instituição de Ensino, fornecer o nome da Instituição de Ensino a qual o(a)  participante estuda;<br>
e)	O gênero literário no qual o texto foi escrito (Poesia ou Conto);<br>
f)	O título do texto;<br>
g)	Caso necessário, isto é, participante menor de 18 anos, anexar a autorização com a assinatura dos pais, responsável ou professor(a) responsável.<br>
h)	Anexar a obra literária escrita pelo(a) participante.<br>
5.3 Não serão aceitas inscrições de textos que expressem valores discriminatórios ou preconceituosos de qualquer tipo, que façam apologia a crimes e contravenções penais ou que atentem contra os princípios e a imagem da Administração Pública.<br>
5.4 O período de inscrição será das 23 horas do dia 01/03/2017 às 23 horas 59 minutos do dia 01/05/2017.<br><br>
6. DO JULGAMENTO E CRITÉRIOS DE AVALIAÇÃO<br><br>
6.1 A avaliação e seleção são prerrogativas exclusivas da comissão Julgadora do Concurso, cujas decisões ocorrerão em absoluto sigilo, levando-se em consideração os critérios estabelecidos neste Edital.<br>
6.2 Cada texto será avaliado por 3 (três) membros da Comissão Julgadora, escolhidos aleatoriamente.<br>
6.3 A avaliação levará em conta os critérios a seguir, que serão pontuados com valores absolutos de 0 (zero) a 10 (dez):<br><br>

a) Adequação ao tema proposto.<br>
b) Emprego de recursos e elementos literários.<br>
c) Criatividade.<br>
d) Uso correto da Língua Portuguesa.<br>
6.4 Após a pontuação de cada critério descrito no Item 6.3, será atribuída a média proporcional, somando-se os quatro valores obtidos e dividindo-se a soma por 4 (quatro).<br>
6.5 As notas atribuídas por cada membro da Comissão Julgadora serão somadas e dividida por 3 (três), obtendo-se a média final.<br><br>
7. DA PREMIAÇÃO<br><br>
7.1 Os 10 participantes com as melhores classificações em cada uma das categorias, segundo decisão da Comissão Julgadora, receberão certificados digitais de participação e terão seus textos publicados na Motus - Movimento Literário Digital.<br>


<br>8. DO CRONOGRAMA<br><br>

Período de inscrição:                                        01/03/2017 a 01/05/2017<br>
Período de avaliação:                                       08/05/2017 a 22/05/2017<br>
Divulgação do resultado preliminar:                24/05/2017<br>
Prazo para recurso:                                           24/05/2017 a 31/05/2017<br>
Divulgação do Resultado final:                        07/06/2017<br>
Premiação e lançamento do livro digital:         30/06/2017<br><br>

9. DAS DISPOSIÇÕES FINAIS <br><br>
9.1 O Programa C tem resguardado o direito de escolha dos membros da Comissão Julgadora, bem como dos critérios de avaliação, cabendo recurso aos participantes, no prazo de 05 (cinco) dias úteis após a publicação dos resultados preliminares, caso haja comprovação do descumprimento de quaisquer das cláusulas deste Edital.<br>
9.2 A Comissão Julgadora será composta por membros de reputação ilibada, com experiência e competência na matéria examinada neste certame.<br>
9.3 Os participantes cujos textos foram classificados para publicação na Motus deverão fornecer uma biografia.<br>
9.4 Os participantes autorizam o uso da imagem e dos textos e não serão remunerados, em hipótese alguma, por qualquer ação atinente ao Concurso, incluindo aparições e divulgação em TV, rádio, mídia digital ou impressa.<br>
9.5 Estarão automaticamente desclassificados, em caráter inapelável e irrecorrível, os participantes que descumprirem este Edital, provocarem atos que venham a prejudicar a realização do Concurso ou que sejam desrespeitosos aos demais participantes, aos membros julgadores, à UNIPAMPA e possíveis parceiros ou patrocinadores.<br>
9.6 Os casos omissos deste Edital serão tratados e analisados pela comissão organizadora do concurso.

